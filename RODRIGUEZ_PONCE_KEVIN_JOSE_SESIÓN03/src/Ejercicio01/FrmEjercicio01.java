    private void btnOrdenarPorPromedio1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenarPorPromedio1ActionPerformed
        int valor = Integer.parseInt(txtNota1.getText());
        int indice = vectorAlumnos.busquedaLinealRecursiva(valor);
        if (indice != -1) {
            txtNombre1.setText(vectorAlumnos.getVector()[indice].getNombre());

        } else {
            
            txtNombre1.setText("No se encuentra");
            txtNota1.requestFocus();

        }
        vectorAlumnos.mostrar(modelo);

    }//GEN-LAST:event_btnOrdenarPorPromedio1ActionPerformed

